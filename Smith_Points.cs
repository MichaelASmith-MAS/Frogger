using UnityEngine;
using System.Collections;
using System.Collections.Generic;

public class Smith_Points : MonoBehaviour 
{
    // ------------------------------------------------------------------------------
    // Public Variables
    // ------------------------------------------------------------------------------

    public int startingPoints = 200;

    // ------------------------------------------------------------------------------
    // Protected Variables
    // ------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------
    // Private Variables
    // ------------------------------------------------------------------------------

    int points;
    bool isDead;

    // ------------------------------------------------------------------------------
    // GETTERS/SETTERS
    // ------------------------------------------------------------------------------

    public int Points
    {
        get { return points; }
    }

    public bool IsDead
    {
        get { return isDead; }
    }

    // ------------------------------------------------------------------------------
    // FUNCTIONS
    // ------------------------------------------------------------------------------

    // ------------------------------------------------------------------------------
    // Function Name: Start
    // Return types: N/A
    // Argument types: N/A
    // Author: 
    // Date: 
    // ------------------------------------------------------------------------------
    // Purpose: Used to initialize variables or perform startup processes
    // ------------------------------------------------------------------------------
    void Start () 
	{
        points = startingPoints;
        isDead = false;

	} //End Start
	
	// ------------------------------------------------------------------------------
	// Function Name: ChangePoints
	// Return types: N/A
	// Argument types: N/A
	// Author: 
	// Date: 
	// ------------------------------------------------------------------------------
	// Purpose: Runs each frame. Used to perform frame based checks and actions.
	// ------------------------------------------------------------------------------
	
	public void ChangePoints (int amount) 
	{
        points = (int)Mathf.Clamp(points + amount, 0, Mathf.Infinity);

        if (points <= 0)
        {
            isDead = true;
        }

	} //End ChangePoints
} // End Smith_Points